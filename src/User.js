export default class User{
    constructor(name, age){
        this.name = name;
        this.age = age;
    }

    toString() {
        const a10 = "a".padStart(10)
        const reg = /(?<year>\d{4})-(?<month>\d{2})-(?<day>\d{2})/;
        const matches = "2018-11-11".match(reg);

        return `${this.name} ${this.age} ${a10}`
    }
}
